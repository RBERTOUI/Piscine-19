int	ft_atoi(char *str)
{
	int i;
	int pon;
	int nbr;

	pon = 1;
	nbr = 0;
	i = 0;
	while (((str[i] == '\t') || (str[i] == '\n') ||
			(str[i] == '\r') || (str[i] == '\v') ||
			(str[i] == '\f') || (str[i] == ' ')))
		i++;
	while ((str[i] == '+') || (str[i] == '-'))
	{
		if (str[i] == '-')
			pon = pon * -1;
		i++;
	}
	while ((str[i] >= '0') && (str[i] <= '9'))
	{
		nbr = (nbr * 10) + (str[i] - '0');
		i++;
	}
	nbr = nbr * pon;
	return (nbr);
}
